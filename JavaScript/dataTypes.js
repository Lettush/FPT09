// Data Types - Primitive Data Types

// Number: Represents numeric values (-12, 45, 1.53)
let age = 25;
console.log("Age:", age);
console.log("Typeof Age:", typeof age);

// Strings: Text Values
let firstName = "John";
console.log("Name:", firstName);
console.log("Typeof Name:", typeof firstName);

// Boolean: true or false
let isAdmin = true;
console.log("Admin?", isAdmin);
console.log("Typeof isAdmin:", typeof isAdmin);

// Null: Intentional Absence of Value
let car = null;
console.log("Do you have a car?", car);

// Undefined: Unintentional Absence of Value
let city;
console.log("City?", city);